import type { AnchorLetter } from './types.js';
import { ANCHOR_LABEL_BY_LETTER } from './types.js';

/** Как якорь читается как фильтр доверия к информации (для §6 карты, раздел «Эпистемический фильтр»). */
export const ANCHOR_FILTER_NARRATIVE_BY_LETTER: Record<AnchorLetter, string> = {
  А: 'Доверие начинается с проверяемых данных и наблюдений; без повторяемости и источников мысль остаётся под вопросом.',
  Б: 'Доверие требует внутренней согласованности и отсутствия логических разрывов; противоречие в аргументации дисквалифицирует вывод.',
  В: 'Доверие опирается на прямое узнавание и целостный гештальт — «знаю», даже когда формулировка ещё не готова.',
  Г: 'Доверие переносится с авторитета — традиции, школы, признанного носителя знания; анонимное или безродное утверждение слабее.',
  Д: 'Доверие закрепляется практикой: работает на деле или нет; теория без проверки действием вторична.',
  Е: 'Доверие сверяется телом — дыхание, напряжение, симметрия состояния; внутренний диссонанс — сигнал остановиться.',
  Ж: 'Доверие вырабатывается в диалоге: позиция живёт, если выдерживает честное столкновение с другим умом.',
  З: 'Доверие связано с целостностью и честностью на уровне ценностей; то, что нарушает этический минимум, отвергается даже при «выгодной» логике.',
};

export function dominantAnchorFilterLine(letter: AnchorLetter | null): string {
  if (letter == null) {
    return 'Доминантный якорь в протоколе не выделен (равномерное распределение букв Я1–Я3). Опирайся на все три ответа блока Я как на спектр фильтров.';
  }
  const label = ANCHOR_LABEL_BY_LETTER[letter];
  const narrative = ANCHOR_FILTER_NARRATIVE_BY_LETTER[letter];
  return `Доминантный якорь по протоколу: ${label} (${letter}). ${narrative}`;
}
