export function getNarrativesForTypes(names: readonly string[]): CognitiveTypeNarrative[] {
  const out: CognitiveTypeNarrative[] = [];
  for (const n of names) {
    const row = COGNITIVE_TYPE_NARRATIVES[n as CognitiveCatalogTypeName];
    if (row) out.push(row);
  }
  return out;
}
