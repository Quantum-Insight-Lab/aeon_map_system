/** Краткая подсказка для буквы профиля на одной полярности. */
export function modalityLetterHint(letter: ModalityLetter): string {
  if (letter === 'А') return 'ближе к первому полюсу таблицы (А)';
  if (letter === 'Б') return 'ближе ко второму полюсу таблицы (Б)';
  return 'ближе к середине между полюсами (М)';
}

export function formatModalityProfileForPrompt(profile: readonly ModalityLetter[]): string {
  const lines: string[] = [];
  for (let i = 0; i < MODALITY_POLES_NARRATIVE.length; i++) {
    const pole = MODALITY_POLES_NARRATIVE[i]!;
    const L = profile[i] ?? '?';
    lines.push(
      `${pole.id} (${pole.title}): выбор «${L}» — ${modalityLetterHint(L as ModalityLetter)}`,
    );
    lines.push(`  Если А: ${pole.aPole}`);
    lines.push(`  Если Б: ${pole.bPole}`);
    lines.push(`  Риски при перегибе на А: ${pole.riskA}`);
    lines.push(`  Риски при перегибе на Б: ${pole.riskB}`);
    lines.push('');
  }
  return lines.join('\n').trim();
}
